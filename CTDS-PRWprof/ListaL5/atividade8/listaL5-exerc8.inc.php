<?php
 function convertemediaAritmetica($n1, $n2, $n3)
  {
  $mediaAritmetica = ($n1 + $n2 + $n3)/3;
  return $mediaAritmetica;
  }

//=====================================================================================================================
 
 function convertemediaPonderada($n1, $n2, $n3)
  {
  $mediaPonderada = (($n1 * 5) + ($n2 * 3) + ($n3 * 2))/(5+3+2);
  return $mediaPonderada;
  }

